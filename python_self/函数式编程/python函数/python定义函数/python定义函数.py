def Add(arg1,arg2):
    """自定义函数，实现加法功能"""
    num1,num2=arg1,arg2
    print("result is ",num1+num2)

Add(10,10)


def Del(arg1,arg2):
    """自定义函数，实现减法功能"""
    num1,num2=arg1,arg2
    print("result is ",num1-num2)

Del(10,10)

def Mul(arg1,arg2):
    """自定义函数，实现乘法功能"""
    num1,num2=arg1,arg2
    print("result is ",num1*num2)

Mul(10,10)



def Div(arg1,arg2):
    """自定义函数，实现除法功能"""
    num1,num2=arg1,arg2
    print("result is ",num1/num2)

Div(10,10)


